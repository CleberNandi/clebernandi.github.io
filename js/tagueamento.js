var captureContact = function click_contact() {
    console.log("Cliquei em 'Entre em Contato")
    ga('send', {
        hitType: 'event',
        eventCategory: 'menu',
        eventAction: 'entre_em_contato',
        eventLabel: 'link_externo'
    });
}

